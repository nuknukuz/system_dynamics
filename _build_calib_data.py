def write_csv(path, header, rows):
    with open(path, "w", newline="", encoding="utf-8") as fh:
        w = csv.writer(fh)
        w.writerow(header)
        for r in rows:
            # дополняем недостающую source-колонку кратким тегом
            row = list(r) + ["GoogleSheets:calibration_table"]
            w.writerow(row)
    print(f"wrote {path}: {len(rows)} rows")


